#!/usr/bin/env python

import os
import sys
import logging
import datetime
import json

if __name__ == '__main__':

    whoami = sys.argv[0]
    whoami = os.path.abspath(whoami)

    bin = os.path.dirname(whoami)
    root = os.path.dirname(bin)

    geometries = os.path.join(root, 'geometries')
    data = os.path.join(root, 'data')

    latest = os.path.join(data, "geometries-spec-latest.json")
    readme = os.path.join(geometries, "README.md")

    fh = open(latest, "r")
    spec = json.load(fh)

    lookup = {}

    for id, details in spec.items():
        lookup[details['name']] = id

    names = lookup.keys()
    names.sort()

    now = datetime.datetime.now()
    ymd = now.strftime("%Y-%m-%d")

    docs = open(readme, "w")
    docs.write("# geometries\n\n")

    docs.write("_This file was generated by the `bin/docs.py` script on %s_\n\n" % ymd)

    for n in names:

        id = lookup[n]
        details = spec[id]

        docs.write("## %s\n\n" % (details['name']))

        if details.get('description'):
            docs.write("_%s_\n\n" % (details['description']))

        for k in ('id', 'name', 'label', 'type'):

            if details[k] == '':
                continue
            docs.write("* %s `%s`\n" % (k, details[k]))

        docs.write("\n")

    docs.close()